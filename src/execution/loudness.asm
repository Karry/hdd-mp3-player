CTI_TABULKU
	movfw ZAZNAM2
	movwf PCLATH
	movfw ZAZNAM1
	movwf PCL

;-----------------------------------------------------------------------

VS_LOAD_PLUGIN		; nahraje plugin do RAM dekodeku
	movlw VSADDR_WRAMADDR 
	movwf TEMP1		; do TEMP1 dame adresu registru 
	movlw 0x00
	movwf TEMP2		; do TEMP2 dame dolni slabiku zapisovaneho slova
	movlw 0x82
	movwf TEMP3		; do TEMP3 dame horni slabiku zapisovaneho slova

	PROG_PAGE_1
	call VS_WR_REG
	PROG_PAGE_3
			; Do VS registru WRAMADDR se nejdrive musi nahrat adresa 0x8200 pro zacatek pluginu na adrese 0x4200 
			; (0x4200 + 0x4000 = 0x8200 vis. "6.5.8 WRAMADDR" datasheetu VS1001k)
			;
	; a ted jdeme nahravat samotny plugin
	; tabulku s pluginem mame na adrese 0x1800..0x1DEB (1516 bytes)
	movlw 0x00
	movwf ZAZNAM1
	movlw 0x18
	movwf ZAZNAM2
VS_LOAD_PLUGIN_LOAD
	movlw VSADDR_WRAM
	movwf TEMP1		; do TEMP1 dame adresu registru 
	call CTI_TABULKU
	movwf TEMP2		; do TEMP2 dame dolni slabiku zapisovaneho slova
	incf ZAZNAM1,f
	call CTI_TABULKU
	movwf TEMP3		; do TEMP3 dame horni slabiku zapisovaneho slova
	PROG_PAGE_1
	call VS_WR_REG
	PROG_PAGE_3

	incf ZAZNAM1,f
	btfsc STATUS,Z
	incf ZAZNAM2,f

	movfw ZAZNAM2
	sublw 0x1D
	btfss STATUS,Z 
	goto VS_LOAD_PLUGIN_LOAD

	movfw ZAZNAM1
	sublw 0xEC
	btfss STATUS,Z 
	goto VS_LOAD_PLUGIN_LOAD

	clrf VS_LOUDNESS
	PROG_PAGE_1
	call VS_SOFT_RESET
	PROG_PAGE_3	
	return
